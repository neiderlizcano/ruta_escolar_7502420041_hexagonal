    private void inicializarEstados() {
        Estados.removeAllItems();
        for (EstadoBus estado : EstadoBus.values()) {
            Estados.addItem(estado.name());
        }
    }

    public void cargarBus(Bus bus) {
        this.busActual = bus;
        if (bus == null) {
            limpiarCampos();
            return;
        }

        CampoPlaca.setText(bus.getPlaca());
        if (bus.getEstado() != null) {
            Estados.setSelectedItem(bus.getEstado().name());
        } else if (Estados.getItemCount() > 0) {
            Estados.setSelectedIndex(0);
        }
    }

    private void configurarEventos() {
        Guardar.addActionListener(e -> onGuardar());
        Limpiar.addActionListener(e -> limpiarCampos());
        Cancelar.addActionListener(e -> onCancelar());
    }

private void onGuardar() {
    if (busActual == null) {
        JOptionPane.showMessageDialog(
                this,
                "No hay bus cargado para actualizar. Selecciona uno desde la lista primero.",
                "Sin bus",
                JOptionPane.WARNING_MESSAGE
        );
        return;
    }

    String nuevaPlaca = CampoPlaca.getText();
    String estadoStr = (String) Estados.getSelectedItem();

    try {
        busActual.actualizarPlaca(nuevaPlaca);

        if (estadoStr != null && !estadoStr.isBlank()) {
            EstadoBus nuevoEstado
                    = EstadoBus.valueOf(estadoStr.trim().toUpperCase());
            busActual.cambiarEstado(nuevoEstado);
        }

        Bus actualizado = actualizarBusUseCase.ejecutar(busActual);

        JOptionPane.showMessageDialog(
                this,
                "Bus actualizado:\n" + actualizado,
                "Ã‰xito",
                JOptionPane.INFORMATION_MESSAGE
        );

    } catch (IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(
                this,
                ex.getMessage(),
                "Error de datos",
                JOptionPane.ERROR_MESSAGE
        );
    } catch (RuntimeException ex) {
        JOptionPane.showMessageDialog(
                this,
                "Error al actualizar el bus en la base de datos.",
                "Error",
                JOptionPane.ERROR_MESSAGE
        );
    }
}



    private void limpiarCampos() {
        CampoPlaca.setText("");
        if (Estados.getItemCount() > 0) {
            Estados.setSelectedIndex(0);
        }
        CampoPlaca.requestFocus();
    }

    private void onCancelar() {
        limpiarCampos();

        java.awt.Window window = SwingUtilities.getWindowAncestor(this);
        if (window != null) {
            window.dispose();
        }

        VentanaPrincipal vp = new VentanaPrincipal();
        vp.setLocationRelativeTo(null);
        vp.setVisible(true);
    }

